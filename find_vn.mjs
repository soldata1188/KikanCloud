const filesWithVn = [];

walkDir('src', (f) => {
    if (!f.match(/\.(ts|tsx)$/)) return;
    let content = fs.readFileSync(f, 'utf-8');
    if (vnPattern.test(content)) {
        filesWithVn.push(f.replace(/\\/g, '/'));
    }
});

